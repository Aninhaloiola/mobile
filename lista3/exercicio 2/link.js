function mudarcor(cor) {
	switch (cor){
		case 'roxo':
			document.getElementById('link').style.backgroundColor = "purple";
			break;
		case'azul':
			document.getElementById('link').style.backgroundColor = 'blue';
			break;
		case 'preto':
			document.getElementById('link').style.backgroundColor = 'black';
			break;
		case 'laranja':
			document.getElementById('link').style.backgroundColor = 'orange';
			break;	
	}

}